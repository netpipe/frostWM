void addMask(Fl_Window *win,char* pixmap, int w, int h)
{
   if ( ! win )
   {
       printf("Null Pointer!\n");
       return;
   }
   Pixmap g_bitmap = XCreateBitmapFromData (fl_display, RootWindow(fl_display, fl_screen), (char
*)pixmap, w, h);
   if( (win == NULL) || (Fl_X::i(win) ==NULL)  )
   {
      printf("Use after window initialization\n");
      return;
   }
   Window wid = Fl_X::i(win)->xid;
   XShapeCombineMask(fl_display,wid , ShapeBounding, 0, 0, g_bitmap, ShapeSet);
   XShapeSelectInput (fl_display, wid, ShapeNotifyMask );
}

